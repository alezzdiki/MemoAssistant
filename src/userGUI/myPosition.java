    /**
     * Se Ã¨ stata trovata una posizione valida, imposta i label
     */
    private void checkPosition(){
    
        if (latitude != 0.0 && longitude != 0.0){
            LatJLabel.setText(String.valueOf(latitude));
            LongJLabel.setText(String.valueOf(longitude));
            addrJLabel.setText(addressFound);
        }
    }
    
    /**
     * Calcola le coordinate dell'indirizzo inserito
     * @throws UnsupportedEncodingException
     * @throws MalformedURLException 
     */
    private void getFromStreet() throws UnsupportedEncodingException, MalformedURLException{
      
       try {
           if (!this.addressTF.getText().isEmpty()) {
               addrJLabel.setText("");
               System.out.println("address inserito: " + addressTF.getText());
               Point2D.Double result = ObjGeocoding.getCoordinates(addressTF.getText());
               if (result == null) {
                   System.out.println("result null");
               }
               System.out.println("sono qui: " + result.toString());
               LatJLabel.setText(String.valueOf(result.x));
               LongJLabel.setText(String.valueOf(result.y));
               addrJLabel.setText(String.valueOf(ObjGeocoding.getAddressFound()));
           }
       }catch (NullPointerException e){
           JOptionPane.showMessageDialog(this, "Check your internet connection", "Error", ERROR_MESSAGE);
       }
   }
