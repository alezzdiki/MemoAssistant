    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        
        try {
            getFromStreet();
            if (!addrJLabel.getText().equalsIgnoreCase("No data") && !addrJLabel.getText().equals("")){
                myPosition.latitude = Double.parseDouble(LatJLabel.getText());
                myPosition.longitude = Double.parseDouble(LongJLabel.getText());
                myPosition.addressFound = addrJLabel.getText();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Address not found. Try again", "Error", ERROR_MESSAGE);
            }
        } catch (UnsupportedEncodingException | MalformedURLException ex) {
            JOptionPane.showMessageDialog(this, "Error geocoding", ex.getMessage(), ERROR_MESSAGE);
        }
    }//GEN-LAST:event_okButtonActionPerformed

